/*2. Write a program to make addition, Subtraction, Multiplication and Division of 
Two Numbers.*/
#include<stdio.h>
void main()
{
	int a,b;
	printf("Enter the value of the a:");
	scanf("%d",&a);
	printf("Enter the value of the b:");
	scanf("%d",&b);
	printf("substraction of two numbers:%d\n",a-b);
	printf("mutiplication of two numbers:%d\n",a*b);
	printf("addition of two numbers:%d\n",a+b);
	printf("division of two numbers:%f\n",(float)a/b);
	
}
